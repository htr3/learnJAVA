package com.learning.java.JAVA.basic.javaBasics;

public class Arrays1 {


    public void array() {
        int[] arr = { 1, 2, 3, 4, 5 };

        // size of array
        int n = arr.length;

        // traversing array
        for (int i = 0; i < n; i++)
            System.out.print(arr[i] + " ");

        //***********************************************


    }




}
